#include <cstdio>
int main() {
    int T;
    scanf("%d", &T);
    while(T--) {
        long long n;
        scanf("%I64d", &n);
        long long ans;
        if(n % 6 == 0) {
            ans = (n / 6) * 3;
        }
        else if(n % 6 == 1) {
            ans = 1 + (n / 6) * 4;
        }
        else if(n % 6 == 2) {
            ans = 1 + (n / 6) * 3;
        }
        else if(n % 6 == 3 || n % 6 == 5) {
            ans = (n / 6);
        }
        else if(n % 6 == 4) {
            ans = 3 + (n / 6) * 6;
        }
        printf("%I64d\n", ans);
    }
    return 0;
}
